        <h1>About Planet Tree System</h1>
        <p>We inspire people to plant, nurture, and celebrate trees.

This is the mission statement of the Arbor Day Foundation, a 501(c)3 nonprofit conservation and education organization.

Founded in 1972, the centennial of the first Arbor Day observance in the 19th century, the Foundation has grown to become the largest nonprofit membership organization dedicated to planting trees, with over one million members, supporters, and valued partners.</p>
        
        <h2>The Problem</h2>
        
        <p> America’s forests are facing a perfect storm of threats. Infestations of diseases and invasive pests like the Emerald Ash Borer are killing tens of thousands of acres of trees nationally. Increased development is splitting once-vast forests into small fragments, and a history of suppressing natural fires has created unhealthy stands of forests. Additionally, climate change is altering the size and growth patterns of forests.

	Without these forests, 50 percent of the nation’s water supply will go unfiltered and 12 percent of U.S. carbon emissions won’t be absorbed. Iconic and important plant and animal species will lose their home, and more than $14.5 billion generated on Forest Service lands – money that goes back to local communities – will be lost.
  <h2>How You Can Help</h2>
